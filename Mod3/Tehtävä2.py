hyttiLuokka = input("Kerropa hyttiluokkasi : ")



if hyttiLuokka == "Lux":
    print("Menkääpä yläkannen parvekkeella olevaan hyttiin")
elif hyttiLuokka =="A":
    print("Menkääpä autokannen yläpuolella olevaan ikkunalliseen hyttiin")
elif hyttiLuokka=="B":
    print("Menkääpä autokannen yläpuolella olevaan ikkunattomaan hyttiin")
elif hyttiLuokka == "C":
    print("Menkääpä autokannen alapuolella olevaan ikkunattomaan hyttiin")
else:
    print("Et ole asiakas. Teidät heitetään mereen")
